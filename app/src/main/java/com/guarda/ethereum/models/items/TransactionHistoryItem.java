package com.guarda.ethereum.models.items;


public class TransactionHistoryItem {

    private long date;
    private double sum;

    public long getDate() {
        return date;
    }

    public void setDate(long date) {
        this.date = date;
    }

    public double getSum() {
        return sum;
    }

    public void setSum(double sum) {
        this.sum = sum;
    }

}
